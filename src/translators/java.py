    def visit_arith_expr(self, node):
        self.visit_binary_op(node)

    @append_to
    def visit_conditional(self, node):
        prev_inside_is = self._inside_is
        self._inside_is = True
        old_ident = self.ident
        self.ident += 2
        children = node.children()

        # Visit children
        cond = children[0]
        cond.accept(self)
        true_branch = children[1]
        false_branch = children[2]
        # Handle Smart Cast and is suffix
        prev_namespace = self._namespace
        if isinstance(cond, ast.Is):
            # true branch smart cast
            if not cond.operator.is_not:
                self._namespace = prev_namespace + ('true_block',)
                self.smart_casts.append((cond.lexpr, cond.rexpr))
                true_branch.accept(self)
                self.smart_casts.pop()
                self._visit_is_stack.pop()
                self._namespace = prev_namespace + ('false_block',)
                false_branch.accept(self)
            # false branch smart cast
            else:
                self._namespace = prev_namespace + ('true_block',)
                self._visit_is_stack.pop()
                true_branch.accept(self)
                self._namespace = prev_namespace + ('false_block',)
                self.smart_casts.append((cond.lexpr, cond.rexpr))
                false_branch.accept(self)
                self.smart_casts.pop()
        else:
            true_branch.accept(self)
            false_branch.accept(self)

        children_res = self.pop_children_res(children)
        res = "{ident}(({if_condition}) ?\n{body} : \n {else_body}){semicolon}".format(
            ident=self.get_ident(old_ident=old_ident),
            if_condition=children_res[0].lstrip(),
            body=children_res[1],
            else_body=children_res[2],
            semicolon=";" if self._parent_is_block() else ""
        )
        self.ident = old_ident
        self._inside_is = prev_inside_is
        self._namespace = prev_namespace
        return res

    @append_to
    def visit_is(self, node):
        old_ident = self.ident
        self.ident = 0

        is_variable = False
        if isinstance(node.lexpr, ast.Variable):
            is_variable = True

        children = node.children()
        children[0].accept(self)
        if is_variable:
            self._visit_is_stack.append(node.lexpr.name)
        for c in children[1:]:
            c.accept(self)
        children_res = self.pop_children_res(children)

        not_1 = "!(" if node.operator.is_not else ""
        not_2 = ")" if node.operator.is_not else ""
        new_var = " {name}{suffix}".format(
                name=node.lexpr.name,
                suffix=self._visit_is_stack.count(node.lexpr.name) * "_is"
            ) if is_variable else ""
        res = "{ident}{not_1}{expr} instanceof {type_to_check}{new}{not_2}".format(
            ident=self.get_ident(old_ident=old_ident),
            not_1=not_1,
            expr=children_res[0],
            type_to_check=node.rexpr.get_name(),
            new=new_var,
            not_2=not_2)
        self.ident = old_ident
        return res

    @append_to
    def visit_new(self, node):
        old_ident = self.ident
        self.ident = 0
        prev_cast_number = self._cast_number
        self._cast_number = True
        children = node.children()
        for c in children:
            c.accept(self)
        children_res = self.pop_children_res(children)
        self.ident = old_ident
        # Remove type arguments from Parameterized Type
        if getattr(node.class_type, 'can_infer_type_args', None) is True:
            cls = node.class_type.name + "<>"
        else:
            cls = self.get_type_name(node.class_type)
        res = "{ident}new {cls}({args}){semicolon}".format(
            ident=self.get_ident(),
            cls=cls,
            args=", ".join(children_res),
            semicolon=";" if self._parent_is_block() else ""
        )
        self._cast_number = prev_cast_number
        return res

    @append_to
    def visit_field_access(self, node):
        old_ident = self.ident
        self.ident = 0
        children = node.children()
        for c in children:
            c.accept(self)
        children_res = self.pop_children_res(children)
        self.ident = old_ident
        receiver = (
            '({})'.format(children_res[0])
            if isinstance(node.expr, ast.BottomConstant)
            else children_res[0]
        )
        return "{ident}{expr}.{field}{semicolon}".format(
            ident=self.get_ident(),
            expr=receiver,
            field=node.field,
            semicolon=";" if self._parent_is_block() else ""
        )

    @append_to
    def visit_func_ref(self, node):
        old_ident = self.ident

        self.ident = 0
        children = node.children()
        for c in children:
            c.accept(self)

        self.ident = old_ident

        children_res = self.pop_children_res(children)

        # Set receiver
        if children_res:
            receiver = children_res[0]
        else:
            # Global function
            receiver = ""
            # Use `this` if its a method within current class
            parent_cls = self.context.get_parent_class(self._namespace)
            if parent_cls:
                class_decls = self.context.get_classes(
                    ('global',), glob=True).values()
                parent_methods = parent_cls.get_callable_functions(class_decls)
                if node.func in {m.name for m in parent_methods}:
                    receiver = "this"
            # Do not use a receiver if its a variable (lambda case)
            decl = get_decl(self.context, self._namespace, node.func)
            if decl:
                namespace, decl = decl
                if namespace == ast.GLOBAL_NAMESPACE and isinstance(
                        decl, ast.FunctionDeclaration):
                    receiver = "Main"

        receiver += "::" if receiver != "" else ""

        res = "{ident}{receiver}{name}{semicolon}".format(
            ident=self.get_ident(),
            receiver=receiver,
            name=node.func,
            semicolon=";" if self._parent_is_block() else ""
        )
        return res

    @append_to
    def visit_func_call(self, node):
        def is_nested_func():
            # fdecl[0][-1] is the parent.
            if fdecl and fdecl[0][-1] != 'global' and fdecl[0][-1][0].islower():
                return True
            return False
        old_ident = self.ident
        self.ident = 0
        prev_cast_number = self._cast_number
        self._cast_number = True

        children = node.children()
        for c in children:
            c.accept(self)

        self.ident = old_ident

        # From where we are in the AST we search backwards for declarations
        # with the same name.
        fdecl = get_decl(self.context, self._namespace, node.func)
        if fdecl and not isinstance(fdecl[1], ast.FunctionDeclaration):
            fdecl = None

        children_res = self.pop_children_res(children)
        func = self._get_main_prefix('funcs', node.func) + node.func
        receiver = children_res[0] if node.receiver else None
        args = children_res[1:] if node.receiver else children_res
        # In case of a nested class with vararg as the last parameter,
        # we have to create an array with the vararg arguments.
        if (is_nested_func() and len(fdecl[1].params) > 0 and
                fdecl[1].params[-1].vararg):
            varargs = args[len(fdecl[1].params)-1:]
            varargs_type = fdecl[1].params[-1].param_type
            if not varargs_type.type_args[0].is_primitive():
                new_stmt = "({etype}) new Object[]".format(
                    etype=self.get_type_name(varargs_type)
                )
            else:
                new_stmt = "new {etype}".format(
                    etype=self.get_type_name(varargs_type)
                )
            varargs = "{new}{{{args}}}".format(
                new=new_stmt,
                args=", ".join(varargs)
            )
            args = args[:len(fdecl[1].params)-1] + [varargs]
        if receiver:
            receiver_expr = (
                '({}).'.format(children_res[0])
                if isinstance(node.receiver, ast.BottomConstant)
                else children_res[0] + '.'
            )
        else:
            receiver_expr = ''
        res = "{ident}{main}{receiver}{name}{apply}({args}){semicolon}".format(
            ident=self.get_ident(),
            main=self._get_main_prefix('vars', node.func) if node.is_ref_call \
                else "",
            receiver=receiver_expr,
            name=func,
            apply=".apply" if is_nested_func() or node.is_ref_call else "",
            args=", ".join(args),
            semicolon=";" if self._parent_is_block() else ""
        )
        self._cast_number = prev_cast_number
        return res

    @append_to
    def visit_assign(self, node):
        old_ident = self.ident
        self.ident = 0
        prev_cast_number = self._cast_number
        self._cast_number = True
        children = node.children()
        for c in children:
            c.accept(self)
        self.ident = old_ident
        children_res = self.pop_children_res(children)
        name = self._get_main_prefix('vars', node.name) + node.name
        receiver = children_res[0] if node.receiver else None
        expr = children_res[1] if node.receiver else children_res[0]
        if receiver:
            receiver_expr = (
                '({}).'.format(children_res[0])
                if isinstance(node.receiver, ast.BottomConstant)
                else children_res[0] + '.'
            )
        else:
            receiver_expr = ''
        res = "{ident}{receiver}{name} = {expr};".format(
            ident=self.get_ident(old_ident=old_ident),
            receiver=receiver_expr,
            name=name,
            expr=expr
        )
        self.ident = old_ident
        self._cast_number = prev_cast_number
        return res
